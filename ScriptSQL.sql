-- HOADON(MaHD, NgayGD, PhuongThucGD, TongTien, MaKH, MaQTV)
CREATE TABLE HOADON (
	MaHD VARCHAR(36) PRIMARY KEY DEFAULT NEWID(),
	NgayGD DATETIME NOT NULL,
	PhuongThucGD VARCHAR(10) NOT NULL,
	TongTien INT NOT NULL,
	MaKH VARCHAR(36),
	MaQTV VARCHAR(36),
	FOREIGN KEY (MaKH) REFERENCES KHACH(MaKH),
	FOREIGN KEY (MaQTV) REFERENCES QUANTRI(MaQTV)
);


